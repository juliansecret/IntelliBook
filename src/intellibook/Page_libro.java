    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
         // Codigo del boton para registrar alumnos
     String titulo = txt_titulo.getText();
    String unidadesStr = txt_unidades.getText();
    String id_libro = txt_IDLibro.getText();

if (titulo.isEmpty() || unidadesStr.isEmpty() || id_libro.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Todos los campos son requeridos");
} else {
    int unidades = Integer.parseInt(unidadesStr);

    // El código restante para registrar el libro
    Libro libro = new Libro();
    libro.setTitulo(titulo);
    libro.setUnidades(unidades);
    libro.setId_libro(id_libro);

    LibroDAO librodao = new LibroDAO();

    if (librodao.registrar(libro)) {
        JOptionPane.showMessageDialog(null, "Libro registrado");
    } else {
        JOptionPane.showMessageDialog(null, "Error al registrar el libro");
    }

    limpiar();


    }//GEN-LAST:event_btn_registrarActionPerformed
    }
    
