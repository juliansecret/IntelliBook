    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        
        // Botón de eliminar
    Libro libro = new Libro();
    LibroDAO librodao = new LibroDAO();

    String IDlibro = txt_buscar.getText();

    if (IDlibro.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Ingrese el ID del libro");
    } else {
        libro.setId_libro(IDlibro);
        if (librodao.eliminar(IDlibro)) {
            JOptionPane.showMessageDialog(null, "Registro eliminado");
        } 
    }
   
    limpiar();
        
        
    }//GEN-LAST:event_btn_eliminarActionPerformed

    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
         // Código del botón buscar
         String id_libro = txt_buscar.getText();
         LibroDAO librodao = new LibroDAO();
         Libro libro = librodao.buscar(id_libro);

    if (libro != null) {
        DefaultTableModel modelo = (DefaultTableModel) tabla_libros.getModel();
        modelo.setRowCount(0);
        Object[] fila = {
            libro.getTitulo(),
            libro.getUnidades(),
            libro.getId_libro()
        };
        modelo.addRow(fila);
    } else {
        JOptionPane.showMessageDialog(null, "Libro no encontrado");
    }
        
    
    }//GEN-LAST:event_btn_buscarActionPerformed

    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        // Codigo del boton para modificar alumnos
    Libro libro = new Libro();
    LibroDAO librodao = new LibroDAO();

    if (txt_buscar.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Ingrese el ID del libro");
    } else {
          String titulo = txt_titulo.getText();
          String unidadesStr = txt_unidades.getText();
          String id_libro = txt_IDLibro.getText();
   
          int unidades = Integer.parseInt(unidadesStr);

          libro.setId_libro(id_libro);
          libro.setUnidades(unidades);
          libro.setTitulo(titulo);

        librodao.modificar(libro);
        JOptionPane.showMessageDialog(null, "Libro modificado");
    }

    limpiar();


    }//GEN-LAST:event_btn_modificarActionPerformed


